    # ─── Euclidean Rhythm Generator ──────────────────────────────────────
    
    def euclidean_generator(self):
        console.print("\n[bold bright_cyan]Euclidean Rhythm Generator[/bold bright_cyan]")
        
        ch = IntPrompt.ask(f"Track channel (current: {self.selected_track})", default=self.selected_track)
        if ch < 1 or ch > 11:
            console.print("[red]Invalid channel[/red]")
            return
        
        steps = IntPrompt.ask("Total steps", default=16)
        pulses = IntPrompt.ask("Pulses (hits)", default=5)
        rotation = IntPrompt.ask("Rotation offset", default=0)
        
        hit_steps = generate_euclidean_rhythm(steps, pulses, rotation)
        
        # Preview
        preview = ""
        for s in range(steps):
            if s in hit_steps:
                preview += "[bold bright_yellow]█[/bold bright_yellow]"
            else:
                preview += "[dim]·[/dim]"
            if s % 4 == 3 and s < steps - 1:
                preview += " "
        
        console.print(f"  Pattern: {preview}")
        console.print(f"  Hits: {len(hit_steps)} / {steps}")
        
        if Confirm.ask("Apply to track?"):
            track = self.project.get_track(ch)
            pattern = track.get_active_pattern()
            bars_needed = max(1, (steps + STEPS_PER_BAR - 1) // STEPS_PER_BAR)
            pattern.bars = min(bars_needed, MAX_BARS)
            
            info = SEQTRAK_TRACKS[ch]
            pitch = 60 if info["type"] == "drum" else note_name_to_midi(f"{self.project.scale_root}3")
            
            for step in hit_steps:
                if step < pattern.total_steps:
                    pattern.add_note(pitch=pitch, step=step, velocity=100)
            
            console.print(f"[green]Applied Euclidean rhythm to {info['name']}[/green]")
    
