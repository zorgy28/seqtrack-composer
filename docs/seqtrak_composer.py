def note_name_to_midi(name: str) -> int:
    """Convert note name like 'C4', 'F#3', 'Bb5' to MIDI note number."""
    name = name.strip()
    if not name:
        raise ValueError("Empty note name")
    
    # Handle flats
    name = name.replace("Bb", "A#").replace("Db", "C#").replace("Eb", "D#")
    name = name.replace("Gb", "F#").replace("Ab", "G#")
    name = name.replace("bb", "A#").replace("db", "C#").replace("eb", "D#")
    name = name.replace("gb", "F#").replace("ab", "G#")
    
    # Parse note and octave
    if len(name) >= 2 and name[1] == '#':
        note_part = name[:2]
        octave_part = name[2:]
    else:
        note_part = name[0].upper()
        octave_part = name[1:]
    
    note_part = note_part.upper()
    if note_part not in NOTE_NAMES:
        raise ValueError(f"Unknown note: {note_part}")
    
    try:
        octave = int(octave_part)
    except ValueError:
        raise ValueError(f"Invalid octave in '{name}'")
    
    return (octave + 1) * 12 + NOTE_NAMES.index(note_part)


def midi_to_note_name(midi_note: int) -> str:
    """Convert MIDI note number to note name like 'C4'."""
    octave = (midi_note // 12) - 1
    note = NOTE_NAMES[midi_note % 12]
    return f"{note}{octave}"


def get_scale_notes(root: str, scale_name: str, octave_start: int = 2, octave_end: int = 6) -> list:
    """Generate all MIDI notes in a scale within the given octave range."""
    if scale_name not in SCALES:
        raise ValueError(f"Unknown scale: {scale_name}")
    
    root_idx = NOTE_NAMES.index(root.upper().replace("b", "#"))
    intervals = SCALES[scale_name]
    notes = []
    
    for octave in range(octave_start, octave_end + 1):
        for interval in intervals:
            midi_note = (octave + 1) * 12 + root_idx + interval
            if 0 <= midi_note <= 127:
                notes.append(midi_note)
    
    return sorted(set(notes))


@dataclass
class Note:
    """A single note event."""
    pitch: int          # MIDI note number (0-127)
    velocity: int = 100 # 1-127
    step: int = 0       # Step position (0-based)
    duration: int = 1   # Duration in steps (1 = 1/16th note)
    probability: int = 100  # 0-100, Seqtrak supports step probability
    
    def to_dict(self):
        return asdict(self)
    
    @classmethod
    def from_dict(cls, d):
        return cls(**d)


@dataclass
class Pattern:
    """A pattern for a single track (up to 8 bars / 128 steps)."""
    name: str = "Pattern 1"
    bars: int = 1          # 1-8 bars
    notes: list = field(default_factory=list)  # List of Note objects
    swing: int = 0         # -100 to +100
    
    @property
    def total_steps(self):
        return self.bars * STEPS_PER_BAR
    
    def add_note(self, pitch: int, step: int, velocity: int = 100, 
                 duration: int = 1, probability: int = 100):
        if step < 0 or step >= self.total_steps:
            raise ValueError(f"Step {step} out of range (0-{self.total_steps-1})")
        if pitch < 0 or pitch > 127:
            raise ValueError(f"Pitch {pitch} out of MIDI range (0-127)")
        velocity = max(1, min(127, velocity))
        
        note = Note(pitch=pitch, velocity=velocity, step=step, 
                    duration=duration, probability=probability)
        self.notes.append(note)
        return note
    
    def remove_note_at(self, step: int, pitch: Optional[int] = None):
        """Remove note(s) at a given step. If pitch is None, remove all notes at that step."""
        self.notes = [n for n in self.notes 
                      if not (n.step == step and (pitch is None or n.pitch == pitch))]
    
    def clear(self):
        self.notes.clear()
    
    def get_step_grid(self) -> dict:
        """Return a dict mapping step -> list of notes at that step."""
        grid = {}
        for note in self.notes:
            grid.setdefault(note.step, []).append(note)
        return grid
    
    def to_dict(self):
        return {
            "name": self.name,
            "bars": self.bars,
            "swing": self.swing,
            "notes": [n.to_dict() for n in self.notes],
        }
    
    @classmethod
    def from_dict(cls, d):
        p = cls(name=d["name"], bars=d["bars"], swing=d.get("swing", 0))
        p.notes = [Note.from_dict(n) for n in d["notes"]]
        return p


@dataclass
class Track:
    """A Seqtrak track with up to 6 patterns."""
    channel: int           # MIDI channel (1-11)
    patterns: list = field(default_factory=list)  # List of Pattern objects
    active_pattern: int = 0
    muted: bool = False
    volume: int = 100      # 0-127
    pan: int = 64          # 0-127 (64 = center)
    
    @property
    def info(self):
        return SEQTRAK_TRACKS[self.channel]
    
    @property
    def name(self):
        return self.info["name"]
    
    @property
    def track_type(self):
        return self.info["type"]
    
    @property
    def color(self):
        return self.info["color"]
    
    def ensure_patterns(self):
        """Ensure at least one pattern exists."""
        if not self.patterns:
            self.patterns.append(Pattern(name=f"Pattern 1"))
    
    def get_active_pattern(self) -> Pattern:
        self.ensure_patterns()
        return self.patterns[self.active_pattern]
    
    def add_pattern(self, name: Optional[str] = None) -> Pattern:
        if len(self.patterns) >= MAX_PATTERNS_PER_TRACK:
            raise ValueError(f"Maximum {MAX_PATTERNS_PER_TRACK} patterns per track")
        idx = len(self.patterns) + 1
        p = Pattern(name=name or f"Pattern {idx}")
        self.patterns.append(p)
        return p
    
    def to_dict(self):
        return {
            "channel": self.channel,
            "active_pattern": self.active_pattern,
            "muted": self.muted,
            "volume": self.volume,
            "pan": self.pan,
            "patterns": [p.to_dict() for p in self.patterns],
        }
    
    @classmethod
    def from_dict(cls, d):
        t = cls(channel=d["channel"], active_pattern=d["active_pattern"],
                muted=d["muted"], volume=d["volume"], pan=d["pan"])
        t.patterns = [Pattern.from_dict(p) for p in d["patterns"]]
        return t


